/// Design system spacing tokens matching web-new CSS.
/// Use these constants instead of magic numbers throughout the app.
abstract final class DesignSpacing {
  /// 4px - Tight spacing, component internal
  static const double xs = 4.0;

  /// 8px - Component internal padding
  static const double sm = 8.0;

  /// 16px - Standard gaps between elements
  static const double md = 16.0;

  /// 24px - Section spacing
  static const double lg = 24.0;

  /// 32px - Large gaps
  static const double xl = 32.0;

  /// 48px - Screen margins, major sections
  static const double xxl = 48.0;
}
